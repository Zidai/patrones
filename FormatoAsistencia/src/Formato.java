    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
         Document document = new Document(); 
        
        try{
            PdfWriter.getInstance(document, new FileOutputStream("/home/subzero/NetBeansProjects/FormatoAsistencia/tablas.pdf"));
            document.open();
            
            // Este codigo genera una tabla de 3 columnas
            PdfPTable table = new PdfPTable(8);                
            
            // addCell() agrega una celda a la tabla, el cambio de fila
            // ocurre automaticamente al llenar la fila
            PdfPCell celdaIni = new PdfPCell(new Paragraph("HOJA DE ASISTENCIA"));
            
            // Indicamos cuantas columnas ocupa la celda
            celdaIni.setColspan(8);
            table.addCell(celdaIni);
             table.addCell("NOMBRE");
                table.addCell("LUNES");
                table.addCell("MARTES");
                table.addCell("MIERCOLES");
                table.addCell("JUEVES");
                table.addCell("VIERNES");
                table.addCell("SABADO");
                table.addCell("DOMINGO");
            int i=0;
            while(i<empleados){
                
                table.addCell((String)datos[i][0]);
                table.addCell((String)datos[i][1]);
                table.addCell((String)datos[i][2]);
                table.addCell((String)datos[i][3]);
                table.addCell((String)datos[i][4]);
                table.addCell((String)datos[i][5]);
                table.addCell((String)datos[i][6]);
                table.addCell((String)datos[i][7]);
                i++;
            }
            
            // Si desea crear una celda de mas de una columna
            // Cree un objecto Cell y cambie su propiedad span
            
            
            
            // Agregamos la tabla al documento            
            document.add(table);
            
            document.close();
            System.out.println("archivo creado con exito");
            JOptionPane.showMessageDialog(null,"Archivo creado con Exito","Exito",JOptionPane.OK_OPTION);
            
        }catch(Exception e)
        {
            System.err.println("Ocurrio un error al crear el archivo");
            JOptionPane.showMessageDialog(null,"Ocurrio un error al crear el archivo","ERROR",JOptionPane.ERROR_MESSAGE);
            System.exit(-1);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

