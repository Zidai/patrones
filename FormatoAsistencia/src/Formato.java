    public Object[][] getDatos(){
    	try{
                
		empleados=0;
		String cadena="";
		File abrir=new File("/home/subzero/NetBeansProjects/FormatoAsistencia/src/empleados.txt");
		FileReader nuevo=new FileReader(abrir);
		BufferedReader cadgen=new BufferedReader(nuevo);
		while((cadena=cadgen.readLine())!=null){
		String [] una=cadena.split(";");
                System.out.println(cadena);
                System.out.println(una[0]);
                System.out.println(una[1]);
		datos[empleados][0]=una[0];
                datos[empleados][1]=una[1];
                datos[empleados][2]=una[1];
                datos[empleados][3]=una[1];
                datos[empleados][4]=una[1];
                datos[empleados][5]=una[1];
                datos[empleados][6]=una[1];
                datos[empleados][7]=una[1];
		empleados++;
                    }
	}
	catch(FileNotFoundException u){
		String e="archivo no existe";
		JOptionPane.showMessageDialog(null,e,"ERROR",JOptionPane.ERROR_MESSAGE);
	}
	catch(IOException una){
		JOptionPane.showMessageDialog(null,"No ingreso ningun archivo","ERROR",JOptionPane.ERROR_MESSAGE);
	}
	catch(ArrayIndexOutOfBoundsException n){
		JOptionPane.showMessageDialog(null,"demasiados elementos para el arreglo");
	}
	catch(NullPointerException otr){
		String x="No se cargo ningun archivo o\nArreglo vacio";
		JOptionPane.showMessageDialog(null,x,"ERROR",JOptionPane.ERROR_MESSAGE);
	}
        return datos;
            }
